/* USER CODE BEGIN 4 */
void AverageTempCalculate(Temp_t* temp)
{
	uint8_t i;
	temp->avg = 0;

	for(i = 0; i < 32; i++)
	{
		if(temp->buff[i] == 0) break;
		temp->avg += temp->buff[i];
	}

	temp->avg /= i;
}

float TempCalculate(uint16_t ADC_value)
{
	float temp = ((((ADC_value * V_REFF / 4095 - V25) / AVG_SLOP) + 25)/2);

	return temp;

}
